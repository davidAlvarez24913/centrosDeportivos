import React, { useEffect, useState } from "react";
import { onAuthStateChanged, User, Auth } from "@firebase/auth";
import { auth } from "../components/Firebase";
import { SignIn } from "../components/Firebase";
import { signOut } from "@firebase/auth";

interface UserContextProps {
  user: User | undefined;
  handleSignIn: (email: string, password: string) => Promise<void>;
  handleSignOut: (auth: Auth) => Promise<void>;
}
export const UserContext = React.createContext<UserContextProps | undefined>(
  {} as UserContextProps
);
export const UserContextProvider = ({
  children,
}: {
  children: React.ReactNode;
}) => {
  const [user, setUser] = useState<User>();

  useEffect(() => {
    const unsubscribe = onAuthStateChanged(auth, (authUser) => {
      if (authUser) {
        setUser(authUser);
      } else {
        setUser(undefined);
      }
    });

    return () => {
      // Desinscribirse de la detecci贸n de cambios en la autenticaci贸n al desmontar el componente
      unsubscribe();
    };
  }, []);

  const handleSignIn = async (email: string, password: string) => {
    try {
      await SignIn(email, password);
    } catch (error) {
      console.error("Error al iniciar sesi贸n:", error);
    }
  };
  const handleSignOut = async (auth: Auth) => {
    try {
      await signOut(auth);
    } catch (error) {
      console.error("Error al cerrar sesi贸n:", error);
    }
  };
  return (
    <UserContext.Provider value={{ user, handleSignIn, handleSignOut }}>
      {children}
    </UserContext.Provider>
  );
};
